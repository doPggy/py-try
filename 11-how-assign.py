## 简单类型
'''
1. 先为值创建内存
2. 内存地址赋值给变量
'''
x = 500
print(id(x))
y = x
print(id(y))
y = 'foo'
print(id(y))

'''
每次生成的值都会新创建一个内存
但是简单的值，会重用内存对象
'''
x = 500
print(id(x))
y = 500
print(id(y))


## 容器类型
'''
1. 500 501 502 创建内存
2. [] 创建内存，把上述三个内存地址存入
3. 赋给 x
'''
x = [500, 501, 502]
print(id(x))
y = x
print(id(y))
y[1] = 600
'''
这样就是重新创建一个 []
地址复制给 y
'''
y = [700, 800]
print(id(y))